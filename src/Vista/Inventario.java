  public void OrdenarCP() {
        
        try {
            con.Conectar();
            PreparedStatement pstm = con.estado().prepareStatement(
                    "Select Nombre_Producto from Producto order by Nombre_Producto;");
            ResultSet res = pstm.executeQuery();
            while (res.next()) {
                String estId = res.getString("Nombre_Producto");
                

                cmbProducto.addItem(estId);
            }
            res.close();
        } catch (SQLException e) {
            System.out.println(e);
        }
  }
  
