    public void LlenarTabla(JTable tablaD) {
        DefaultTableModel modeloTabla = new DefaultTableModel();
        tablaD.setModel(modeloTabla);
        modeloTabla.addColumn("Id");
        modeloTabla.addColumn("Fecha");
        modeloTabla.addColumn("Nombre_producto");
        modeloTabla.addColumn("Producto_existente");
         
        

      TableColumnModel columnModel = jtbDatos.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(30);
        columnModel.getColumn(1).setPreferredWidth(200);
        columnModel.getColumn(2).setPreferredWidth(200);
        columnModel.getColumn(3).setPreferredWidth(200);
       
        
        

        Object[] columna = new Object[4];
        int numReg = Inv.listInvent().size();

        for (int i = 0; i < numReg; i++) {
            columna[0] = Inv.listInvent().get(i).getidInventario();
            columna[1] = Inv.listInvent().get(i).getFecha();
            columna[2] = Inv.listInvent().get(i).getNombre_Producto();
            columna[3] = Inv.listInvent().get(i).getExistente();
            
           
            
            
            modeloTabla.addRow(columna);
        }
        
    }
 public void pasarDatos(JTable tablaD) {
        int fila = tablaD.getSelectedRow();

        txtId.setText(tablaD.getModel().getValueAt(fila, 0).toString());
        jDate_Chooser.setDateFormatString(tablaD.getModel().getValueAt(fila, 1).toString());
        cmbProducto.setSelectedItem(tablaD.getModel().getValueAt(fila, 2).toString());
        txtExistente.setText(tablaD.getModel().getValueAt(fila, 3).toString());
       
    }
 
